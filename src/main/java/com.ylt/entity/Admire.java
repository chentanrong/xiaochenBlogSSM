package com.ylt.entity;

public class Admire {

    private String admirId;

    private String userId;

    private String admiredId;

    private String admirDate;

    private String type;

    public String getAdmirId() {
        return admirId;
    }

    public void setAdmirId(String admirId) {
        this.admirId = admirId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getAdmiredId() {
        return admiredId;
    }

    public void setAdmiredId(String admiredId) {
        this.admiredId = admiredId;
    }

    public String getAdmirDate() {
        return admirDate;
    }

    public void setAdmirDate(String admirDate) {
        this.admirDate = admirDate;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
